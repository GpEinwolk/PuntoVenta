    private void botonSeleccionar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeleccionar
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int se = fc.showSaveDialog(null);
        if(se == JFileChooser.APPROVE_OPTION){
            String ruta = fc.getSelectedFile().getPath();
            textRuta.setText(ruta);
        }
    }//GEN-LAST:event_botonSeleccionar

    private void exportar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportar
        // TODO add your handling code here:
        String ruta = textRuta.getText();
        String name = "\\backup.sql";
        String backus = "";
        if(ruta.trim().length()!=0){
            try{
                backus = "C:\\xampp\\mysql\\bin\\mysqldump --opt -u"+Conexion.getuser()+" -p"+Conexion.getpassword()+" -B "+Conexion.getdb()+" -r "+ruta+name;
                Runtime rt = Runtime.getRuntime();
                rt.exec(backus);
                ImageIcon ua = new ImageIcon("src/img/success.png");
                JOptionPane.showMessageDialog(null, "Backup creado: "+ruta, "Mensaje", JOptionPane.OK_OPTION, ua);
                dispose();
            }catch(Exception ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_exportar

